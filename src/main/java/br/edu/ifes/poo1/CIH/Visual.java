    private void btn_jogadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_jogadaActionPerformed
        String jogada = lbl_jogada.getText();
        if ((jogada.length() == 4) || ((jogada.length() == 5)&& (jogada.charAt(2)=='x'))) {
            control.controlaJogadas(jogada);
            
            atualizaTabuleiro();
        }  else {
            switch (jogada) {
                case "desistir":
                    opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente DESISTIR???", "ATENO", JOptionPane.YES_NO_OPTION);

                    if (opcao == 0) {
                        this.dispose();
                        control.controlaJogadas("desistir");
                    } else {
                        lbl_jogada.setText("");
                    }
                    break;
                case "empate":
                    control.textual = false;
                    control.alteraVez();
                    atualizaTabuleiro();
                    if (control.vezBranco) {
                        opcao = JOptionPane.showConfirmDialog(null, "##EMPATE##\n" + control.nomeJogador[0] + " deseja Aceitar o"
                                + "\nempate proposto por " + control.nomeJogador[1], "ATENO", JOptionPane.YES_NO_OPTION);
                        if (opcao == 0) {
                            this.dispose();
                            control.controlaJogadas("empate");
                        } else {
                            control.alteraVez();
                            atualizaTabuleiro();
                        }

                    } else {
                        opcao = JOptionPane.showConfirmDialog(null, "##EMPATE##\n" + control.nomeJogador[1] + " deseja Aceitar o"
                                + "\nempate proposto por " + control.nomeJogador[0], "ATENO", JOptionPane.YES_NO_OPTION);
                        if (opcao == 0) {
                            fechaTela();
                            control.controlaJogadas("empate");
                        } else {
                            control.alteraVez();
                            atualizaTabuleiro();
                        }
                    }

                    break;
            }
        }
        alertaOpcaoInvalida();

    }//GEN-LAST:event_btn_jogadaActionPerformed

