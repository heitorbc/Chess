    private String descobreCasa(String lbl) {
        switch (tabuleiro.retornaPecaVIsual(lbl)) {
            case " T ":
                if ("(B)".equals(tabuleiro.retornaPeca(lbl).getCor().getCorPeca())) {
                    return "tb";
                } else {
                    return "tp";
                }
            case " C ":
                if ("(B)".equals(tabuleiro.retornaPeca(lbl).getCor().getCorPeca())) {
                    return "cb";
                } else {
                    return "cp";
                }
            case " B ":
                if ("(B)".equals(tabuleiro.retornaPeca(lbl).getCor().getCorPeca())) {
                    return "bb";
                } else {
                    return "bp";
                }
            case " D ":
                if ("(B)".equals(tabuleiro.retornaPeca(lbl).getCor().getCorPeca())) {
                    return "qb";
                } else {
                    return "qp";
                }
            case " K ":
                if ("(B)".equals(tabuleiro.retornaPeca(lbl).getCor().getCorPeca())) {
                    return "kb";
                } else {
                    return "kp";
                }
            case " P ":
                if ("(B)".equals(tabuleiro.retornaPeca(lbl).getCor().getCorPeca())) {
                    return "pb";
                } else {
                    return "pp";
                }
            case "":
                return "";
        }
        return "";
    }

